  <h2>ISTQB</h2>

  <p class="section-intro">
    ISTQB (International Software Testing Qualifications Board) to międzynarodowy standard
    certyfikacji dla profesjonalistów zajmujących się testowaniem oprogramowania.
    PQB natomiast to jedyna reprezentacja Rady Krajowej ISTQB w Polsce, odpowiedzialna za organizację egzaminów i akredytację szkoleń w naszym kraju. W ramach naszego portfolio znajdziesz wszystkie niezbędne materiały, takie jak sylabusy, pytania egzaminacyjne, odpowiedzi oraz wytyczne do akredytacji, które pomogą Ci przygotować się do certyfikacji ISTQB i rozwijać swoją karierę w testowaniu oprogramowania. Zapraszamy do zapoznania się z naszymi zasobami i korzystania z nich w swojej drodze do sukcesu w branży testowania oprogramowania!
  </p>

